const game = new Game(stage);

let startButton;
let stopButton;
let resetButton;
let playbackButton;

function setup() {
  createCanvas(300, 600);
  startButton = createButton("START");
  startButton.mousePressed(() => {
    game.start();
  });

  resetButton = createButton("RESET");
  resetButton.mousePressed(() => {
    game.reset(stage);
  });

  playbackButton = createButton("PLAYBACK");
  playbackButton.mousePressed(() => {
    game.playback();
  });
}

function draw() {
  background(255);
  frameRate(30);

  game.main();
  game.draw("block");
}
